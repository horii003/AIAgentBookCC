"""
交通費申請ポリシー

このモジュールは、交通費精算申請エージェント（AG-002）が使用するルールテキストを提供します。
"""


def get_transportation_policies() -> str:
    """
    交通費申請ルールのテキストを取得する。

    Returns:
        str: 交通費申請ルール（マークダウン形式）
    """
    return """## 交通費精算申請ルール

### BRL-01: 申請ルール参照による回答・判断
- 申請種別の判断および申請ルール照会の回答は、社内申請ルールのみを根拠として行う。
- 社内申請ルールに該当情報がない場合は BRL-14 を適用し、推測・補完を行わない。

### BRL-02: 不足情報の対話確認
- 申請書作成に必要な必須項目に不足がある場合は、不足項目を一つずつ対話で確認する。
- 全必須項目が収集済みの場合は BRL-07 を適用する。

### BRL-03: 申請内容妥当性チェック実施
- 申請書（下書き）生成後、社内申請ルールに基づく妥当性チェックを実施し、結果と修正案を提示する。

### BRL-05: 申請種別の確認
- 申請内容が交通費精算申請に該当することを確認してから処理を開始する。

### BRL-06: 申請者情報の確認
- invocation_state から受け取った申請者名・申請日を使用し、社員への確認は省略してよい。

### BRL-07: 全必要情報収集後の申請書生成
- 全必須項目（移動日・出発地・目的地・交通手段・費用・業務目的）が収集済みであること。
- APR-001 承認が完了していること。
- 上記両条件を満たした場合のみ generate_transport_application（TOOL-002）を呼び出す。

### BRL-08: 一括収集設計（対話回数削減）
- 1区間分の情報（移動日・出発地・目的地・交通手段・業務目的）を1ターンで一括提示して収集する。
- 項目を1つずつ順番に聞いてはいけない。

### BRL-10: 交通費移動情報の一区間ずつ収集
- 移動情報は一区間ごとに（移動日・出発地・目的地・交通手段）を個別に確認・収集する。
- 区間収集完了後、次の区間の有無を社員に確認する。
- 複数区間のまとめ入力は認めない。

### BRL-11: 申請期限超過検出・警告
- 移動日から申請日までが3ヶ月（90日）を超える場合は申請期限超過の警告を提示する。
- 警告後も申請継続可否は社員の判断に委ねる（強制停止しない）。
- 警告メッセージ例: 「この移動日は申請期限（90日）を超過しています。管理部門（経理部）にご相談ください。」

### BRL-12: 高額申請時の事前上長承認案内
- 交通費精算申請で申請金額合計が 10,000 円を超える場合は事前上長承認が必要な旨を案内する。
- 案内のみ行い、承認申請は社員が実施する。
- 案内メッセージ例: 「費用が10,000円を超えています。申請前に上長の事前承認を取得していることを確認してください。」

### BRL-13: 業務目的記載必須
- 業務目的欄は全区間で必須項目であり、空欄の場合はNGと判定して記載を促す。
- 申請書生成前に全区間の業務目的が記載済みであることを確認する。

### BRL-14: 申請ルール外内容の推測・補完禁止
- 社内申請ルールに定義されていない内容については推測・補完せず、管理部門への問い合わせを案内する。

### BRL-16: 申請書生成後の自動チェック実行
- 申請書（下書き）生成後、自動的に申請内容チェックを実行する。
- チェック観点: 全区間に業務目的が記載されていること（BRL-13）、申請期限内であること（BRL-11）、高額申請の事前上長承認確認（BRL-12）。

### BRL-17: 運賃データからの交通費自動計算
- 各区間の移動情報収集後に calculate_transport_fare（TOOL-001）を呼び出して運賃を自動計算する。
- 電車: 経路テーブル（出発地・目的地→運賃）から自動計算する。
- バス・タクシー・飛行機: 固定運賃データを参照する。
- 該当経路が存在しない場合（calculable=False）は社員に手動入力を促す（JD-16）。

### BRL-18: 駅名正規化
- 社員が入力した駅名から「駅」の接尾辞を除去して正規化する（例: 「渋谷駅」→「渋谷」）。
- TOOL-001 呼び出し前に正規化を完了させること。
"""
