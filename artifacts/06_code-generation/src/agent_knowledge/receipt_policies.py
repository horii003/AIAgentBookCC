"""
経費申請ポリシー

このモジュールは、経費精算申請エージェント（AG-003）が使用するルールテキストを提供します。
"""


def get_receipt_policies() -> str:
    """
    経費申請ルールのテキストを取得する。

    Returns:
        str: 経費申請ルール（マークダウン形式）
    """
    return """## 経費精算申請ルール

### BRL-07: 全必要情報収集後の申請書生成
- 全必須項目（購入日・金額・店舗名・品目・経費区分・業務目的）が収集済みであること。
- APR-001 承認が完了していること。
- 上記両条件を満たした場合のみ generate_expense_application（TOOL-002）を呼び出す。

### BRL-09: 品目からの経費区分自動判断
- 社員が入力した品目名称を経費区分定義と照合し、経費区分を自動判断する。
- 判断ルール:
  - 「ノート」「ボールペン」「用紙」「文具」「事務用品」→ 事務用品費
  - 「ホテル」「宿泊」「宿」→ 宿泊費
  - 「試験」「受験」「資格」「検定」「認定」→ 資格精算費
  - 上記以外または複数区分に該当する場合→ 判断困難として社員に4択を提示
- 判断困難時の4択提示メッセージ: 「品目から経費区分を特定できませんでした。「事務用品費」「宿泊費」「資格精算費」「その他経費」のいずれかを選択してください。」
- 社員が「その他経費」を選択した場合はそのまま使用する。

### BRL-11: 申請期限超過検出・警告
- 購入日から申請日までが3ヶ月（90日）を超える場合は申請期限超過の警告を提示する。
- 警告後も申請継続可否は社員の判断に委ねる（強制停止しない）。
- 警告メッセージ例: 「この購入日は申請期限（90日）を超過しています。管理部門（経理部）にご相談ください。」

### BRL-12: 高額申請時の事前上長承認案内
- 経費精算申請で申請金額が 5,000 円を超える場合は事前上長承認が必要な旨を案内する。
- 案内のみ行い、承認申請は社員が実施する。
- 案内メッセージ例: 「申請金額が5,000円を超えています。申請前に上長の事前承認を取得していることを確認してください。」

### BRL-13: 業務目的記載必須
- 業務目的欄は全経費で必須項目であり、空欄の場合はNGと判定して記載を促す。
- 申請書生成前に全経費の業務目的が記載済みであることを確認する。
"""
