# 交通費申請の妥当性チェックルール（BRL-03/BRL-06/BRL-11/BRL-12等）
TRANSPORTATION_POLICIES = """【交通費申請 妥当性チェックルール】

■ 必須項目（BRL-06）
交通費精算申請には以下の情報がすべて必要です:
- 移動日（YYYY-MM-DD形式）
- 出発地
- 目的地
- 交通手段（電車/バス/タクシー/飛行機）
- 費用（運賃）
- 業務目的

■ 申請期限（BRL-12）
- 移動日から申請日まで90日以内であること
- 期限超過の場合は申請不可（担当部署へ相談を案内）

■ 申請書チェック義務（BRL-03）
申請書作成後、以下を順にチェックすること:
1. 必須項目チェック: 全項目が揃っていること
2. 整合性チェック: 移動日と申請日の整合性、費用が正の整数
3. ルール適合性チェック: 業務目的が業務上合理的であること
4. 上長承認要否チェック: 交通費合計 > 10,000円は上長承認必要

■ 上長承認（BRL-11）
- 交通費合計が10,000円を超える場合は上長承認が必要
- 対象の場合は申請書チェック後に社員へ案内すること

■ 駅名表記（BRL-15）
- 出発地・目的地の「駅」「駅前」等の接尾語は自動除去する
- 例: 「渋谷駅」→「渋谷」、「新宿駅前」→「新宿」

■ 交通手段表記（BRL-14）
- 「電車」「鉄道」「JR」「地下鉄」→「電車」に統一
- 「バス」「路線バス」→「バス」に統一
- 「タクシー」「taxi」→「タクシー」に統一
- 「飛行機」「航空機」→「飛行機」に統一
"""


def get_transportation_policies() -> str:
    """交通費申請の妥当性チェックルールを返す。

    Returns:
        str: 妥当性チェックルール文字列
    """
    return TRANSPORTATION_POLICIES
