"""経費申請の妥当性チェックルール定義

AG-003（経費精算申請エージェント）のシステムプロンプトに組み込む
妥当性チェックルールを定義する。ルール変更時はこのファイルのみ更新する。
"""

# 経費申請の妥当性チェックルール（BRL-03/BRL-07/BRL-12/BRL-17/BRL-18等）
RECEIPT_POLICIES = """【経費申請 妥当性チェックルール】

■ 必須項目（BRL-07）
経費精算申請には以下の情報がすべて必要です:
- 店舗名
- 経費区分（事務用品費/宿泊費/資格精算費/その他経費）
- 金額（円単位・1円以上の正整数）
- 経費発生日（YYYY-MM-DD形式）
- 業務目的

■ 申請期限（BRL-12）
- 経費発生日から申請日まで90日以内であること
- 期限超過の場合は申請不可（担当部署へ相談を案内）

■ 経費区分（BRL-17）
以下の4区分に分類する。判断不可の場合は「その他経費」として扱う（エラーにしない）:
- 事務用品費: 文具・消耗品等の購入費用（「事務用品」「文具」→事務用品費）
- 宿泊費: 出張時の宿泊費用（「宿泊」「ホテル」→宿泊費）
- 資格精算費: 業務に関連する資格取得費用（「資格」「試験」「検定」→資格精算費）
- その他経費: 会食費・接待費等の交通費以外の経費

■ 申請書チェック義務（BRL-03）
申請書作成後、以下を順にチェックすること:
1. 必須項目チェック: 全項目が揃っていること
2. 整合性チェック: 経費発生日と申請日の整合性、金額が正の整数
3. ルール適合性チェック: 業務目的が業務上合理的であること
4. 上長承認要否チェック: 経費金額 > 5,000円は上長承認必要

■ 上長承認（BRL-18）
- 経費金額が5,000円を超える場合は上長承認が必要
- 対象の場合は申請書チェック後に社員へ案内すること

■ 領収書画像対応（BRL-16）
- 社員が領収書画像を提供した場合はimage_readerで自動読み取りを試みる
- 読み取り失敗時は手動入力を促す
"""


def get_receipt_policies() -> str:
    """経費申請の妥当性チェックルールを返す。

    Returns:
        str: 妥当性チェックルール文字列
    """
    return RECEIPT_POLICIES
